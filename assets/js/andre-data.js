// Rarity weights untuk gacha
const RARITY_WEIGHTS = {
  common: 40,
  uncommon: 30,
  rare: 20,
  epic: 8,
  legendary: 2
};

const RARITY_COLORS = {
  common: { bg: '#78909C', text: '#ECEFF1', label: 'Common' },
  uncommon: { bg: '#66BB6A', text: '#FFFFFF', label: 'Uncommon' },
  rare: { bg: '#42A5F5', text: '#FFFFFF', label: 'Rare' },
  epic: { bg: '#AB47BC', text: '#FFFFFF', label: 'Epic' },
  legendary: { bg: 'linear-gradient(135deg, #FF6F00, #FFD600)', text: '#1A1A1A', label: '⭐ Legendary ⭐' }
};

function rollGacha() {
  // Build weighted pool
  const pool = [];
  ANDRE_COLLECTION.forEach(andre => {
    const weight = RARITY_WEIGHTS[andre.rarity] || 10;
    for (let i = 0; i < weight; i++) {
      pool.push(andre);
    }
  });
  const picked = pool[Math.floor(Math.random() * pool.length)];
  const image = picked.images[Math.floor(Math.random() * picked.images.length)];
  const imagePath = picked.folder
    ? `assets/img/${picked.folder}/${image}`
    : `assets/img/${image}`;
  return { ...picked, selectedImage: imagePath };
}
